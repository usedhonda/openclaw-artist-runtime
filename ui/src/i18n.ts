import type { UiLocaleMode } from "../../src/types";

export type ProducerRoomLocale = "ja" | "en";

export function resolveProducerRoomLocale(mode: UiLocaleMode | undefined, browserLanguage?: string): ProducerRoomLocale {
  if (mode === "ja" || mode === "en") {
    return mode;
  }
  const language = browserLanguage ?? (typeof navigator !== "undefined" ? navigator.language : "");
  return language.toLowerCase().startsWith("ja") ? "ja" : "en";
}

